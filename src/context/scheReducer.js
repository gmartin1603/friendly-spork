const scheReducer = (state, action) => {
    // console.log(action, state)
    // console.log(action)
    switch(action.type) {

        case "SET-TEXT":
            return (
                {
                    ...state,
                    [action.name]: action.change
                }
            )
        case "SET-FORM-STATE":

            return (
                {
                    ...state,
                    formObj: action.load,
                    showForm:true
                }
            )
        case "SET-ARR":
            return (
                {
                    ...state,
                    [action.name]: action.load,
                }
            )
        
        case "CLOSE-FORM":
            return (
                {
                    ...state,
                    showForm: false,
                    formObj: {},
                }
            )
        default: 
            console.log("No Form State Change")
            return state;
    }
    
}

export default scheReducer